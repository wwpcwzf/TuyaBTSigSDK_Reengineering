/*-------------------------------------------------------------------------
*简  介: 
*参  数: 
*返回值: 
-------------------------------------------------------------------------*/
u8 lutec_check_sum(u8* data, u8 len)
{
	u8 i, sum = 0;
	for(i = 0; i < len; i++)
	{
		sum += data[i];
	}
	return sum;
}



/*-------------------------------------------------------------------------
*简  介: 
*参  数: 
*返回值: 
-------------------------------------------------------------------------*/
u8 lutec_string_compare(u8* str1_ptr, u8* str2_ptr, u8 str_len)
{
    for(u8 sub_j = 0; sub_j < str_len; sub_j++)
    {
        if(str1_ptr[sub_j] != str2_ptr[sub_j])
        {
            return 0;
        }
    }
    return 1;
}
























